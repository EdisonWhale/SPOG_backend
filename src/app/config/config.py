class EntraConfig(BaseModel):
    """Configuration for validating Microsoft Entra (Azure AD) access tokens."""

    enabled: Annotated[bool, Field(
        default=True,
        description="Whether to validate the Authorization bearer token. Disable for local testing.",
    )]

    tenant_id: Annotated[str, Field(
        default="common",
        description="Microsoft Entra tenant ID (directory ID).",
    )]

    audience: Annotated[str, Field(
        default="",
        description="Expected token audience (the application/client ID or API App ID URI).",
    )]

    jwks_uri: Annotated[str, Field(
        default="",
        description="Override for the JWKS URI. If empty, it is derived from tenant_id.",
    )]

    verify_signature: Annotated[bool, Field(
        default=True,
        description="Whether to verify the token signature against the Entra JWKS.",
    )]

    @computed_field
    @property
    def openid_config_url(self) -> str:
        return f"https://login.microsoftonline.com/{self.tenant_id}/v2.0/.well-known/openid-configuration"

    @computed_field
    @property
    def default_jwks_uri(self) -> str:
        if self.jwks_uri:
            return self.jwks_uri
        return f"https://login.microsoftonline.com/{self.tenant_id}/discovery/v2.0/keys"

    @computed_field
    @property
    def issuer(self) -> str:
        return f"https://login.microsoftonline.com/{self.tenant_id}/v2.0"


