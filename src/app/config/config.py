def load_config() -> Config:
    try:
        logger.info(f"K_SERVICE: {os.environ.get('K_SERVICE')}")
        K_SERVICE: str = os.environ.get("K_SERVICE", os.environ.get("JOB_ENV", "local"))
        logger.info(f"K_SERVICE JOB_ENV: {K_SERVICE}")
        config_file_dir = os.path.dirname(os.path.abspath(__file__))
        logger.info(f"config_file_dir: {config_file_dir}")
        return Config(_env_file=os.path.join(config_file_dir, f".env.{K_SERVICE}"))
    except Exception as e:
        print(f"Failed to load configuration: {e}")

def load_gcp_config() -> GoogleCloudPlatformConfig:
    config = load_config()
    return config.gcp_config

def load_cache_config() -> CacheConfig:
    config = load_config()
    return config.cache_config

def load_entra_config() -> EntraConfig:
    config = load_config()
    entra = config.entra_config
    # The OAUTH__ env keys (oauth_config) drive enable/verify for local dev;
    # overlay them onto the Entra config used by the auth dependency.
    oauth = config.oauth_config
    return entra.model_copy(update={
        "enabled": oauth.enabled,
        "verify_signature": oauth.verify_signature,
    })

def load_gitlab_config() -> GitLabConfig:
    config = load_config()
    return config.gitlab_config

